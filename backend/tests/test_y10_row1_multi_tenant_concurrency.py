def test_dimensions_match_acceptance_criterion():
    """Lock the (10, 20, 5, 1000) tuple against drift.

    The TODO row literally states ``10 tenant × 20 project × 5
    user`` — if a future refactor lowers any dimension under load
    pressure (e.g. someone pin-pricks the test scale to make CI
    faster) the acceptance criterion silently weakens. This guard
    catches that and forces the change to be discussed in the
    commit message.
    """
    assert _TENANTS_N == 10
    assert _PROJECTS_PER_TENANT == 20
    assert _USERS_PER_TENANT == 5
    assert _TOTAL_RUNS == 1000
    assert (
        _TENANTS_N * _PROJECTS_PER_TENANT * _USERS_PER_TENANT
        == _TOTAL_RUNS
    )


def test_workflow_finish_reads_attribution_from_db_row_not_contextvar():
    """Source-grep: ``workflow.finish`` MUST pass the
    ``(tenant_id, project_id)`` tuple it reads off the
    ``workflow_runs`` row directly to ``billing_usage.record_workflow_run``,
    not from the request-scope ContextVar.

    Why this matters for Y10 row 1: the concurrent fan-out runs
    ``workflow.finish`` from background tasks where the original
    request scope may already have torn down. If a future refactor
    swapped the explicit row-read for a ``current_tenant_id()``
    call, billing rows would silently mis-attribute under
    concurrency (tasks finishing on a stale ContextVar slot picked
    up someone else's tenant).
    """
    from backend import workflow

    src = inspect.getsource(workflow.finish)
    # The fan-out site explicitly threads tenant_id / project_id
    # from the SELECT row (not from set_tenant_id contextvar).
    assert "tenant_id=row[\"tenant_id\"]" in src, (
        "workflow.finish must pass tenant_id=row['tenant_id'] to "
        "record_workflow_run — Y10 row 1 attribution invariant"
    )
    assert "project_id=row[\"project_id\"]" in src, (
        "workflow.finish must pass project_id=row['project_id'] to "
        "record_workflow_run — Y10 row 1 attribution invariant"
    )


def test_audit_chain_lock_is_keyed_per_tenant_not_global():
    """Source-grep: ``audit._log_impl`` advisory lock must hash a
    per-tenant key, NOT a global constant.

    Cross-tenant deadlock prevention: 10 tenants writing audit
    rows concurrently must not serialise on a shared lock — each
    tenant's chain has its own key space. If someone accidentally
    promoted the key to ``"audit-chain"`` (no tenant interpolation)
    the 1000-task fan-out would degrade to single-threaded
    throughput and the timeout guard would trip.
    """
    from backend import audit

    src = inspect.getsource(audit._log_impl)
    assert "pg_advisory_xact_lock(hashtext($1))" in src, (
        "audit._log_impl must use pg_advisory_xact_lock(hashtext($1))"
    )
    # The key must contain the tenant id — search for the f-string
    # that builds 'audit-chain-{tid}'.
    assert 'f"audit-chain-{tid}"' in src, (
        "audit._log_impl advisory lock key must be per-tenant "
        "(f\"audit-chain-{tid}\") — Y10 row 1 deadlock prevention"
    )


def test_billing_resolver_does_not_overwrite_explicit_project():
    """Negative control for the cross-talk hypothesis: if a task
    sets ``set_project_id("p-other")`` in its ContextVar but
    ``workflow.finish`` passes ``project_id="p-explicit"``
    explicitly to ``record_workflow_run``, the resolver must
    return ``"p-explicit"`` — explicit always wins over ContextVar.

    Pure-unit because the resolver is a closed-form function;
    asserting this contract in isolation guarantees Block B's
    1000-task assertion that "every billing row's project_id
    matches the workflow_runs.project_id" holds even under heavy
    contextvar swapping.
    """
    from backend import billing_usage as bu
    from backend.db_context import set_project_id, set_tenant_id

    # Set ContextVar to a different value to prove explicit wins.
    set_tenant_id("t-ctx-leaked")
    set_project_id("p-ctx-leaked")
    try:
        resolved = bu._resolve_project(
            "p-explicit-wins", tenant_id="t-explicit-wins",
        )
        assert resolved == "p-explicit-wins"
        # And the resolver does NOT touch tenant either when explicit.
        assert bu._resolve_tenant("t-explicit-wins") == "t-explicit-wins"
    finally:
        set_tenant_id(None)
        set_project_id(None)


@pytest.mark.asyncio
async def test_contextvar_per_task_isolation_under_gather_no_pg():
    """Pure-asyncio test (no PG): N concurrent tasks each set a
    distinct ``tenant_id`` in their own ContextVar scope and read
    it back after ``await asyncio.sleep(0)`` to force a scheduler
    yield. The ContextVar-per-task guarantee is what the entire
    Y row stack rests on; if it broke, every Block B assertion
    would be silently meaningless.

    Lifted from ``test_multi_tenant_isolation.py`` and made
    asyncio-only here so the contract is checked even on a CI
    runner without PG.
    """
    from backend.db_context import current_tenant_id, set_tenant_id

    async def _task(tid: str) -> str:
        set_tenant_id(tid)
        # Force the scheduler to run other tasks before we read back
        # — this is where a leaky shared dict (instead of a
        # contextvars.ContextVar) would surface as cross-task bleed.
        await asyncio.sleep(0)
        seen = current_tenant_id()
        return f"{tid}|{seen}"

    tids = [f"t-isolation-{i:02d}" for i in range(20)]
    random.shuffle(tids)
    results = await asyncio.gather(*[_task(t) for t in tids])
    for line in results:
        sent, seen = line.split("|", 1)
        assert sent == seen, (
            f"ContextVar leak: task set {sent!r} but read back "
            f"{seen!r} — Y10 row 1 isolation invariant violated"
        )


def test_per_tenant_audit_chain_lock_key_distinct_across_tenants():
    """If two distinct tenant_ids hash to the SAME advisory-lock
    key, audit writes on those two tenants would block each other
    cross-tenant — silently violating the "no deadlock under
    concurrent load" Y10 row 1 acceptance.

    PG's ``hashtext()`` is a 32-bit hash; collisions in a 10-tenant
    test set are astronomically unlikely but the test fixture
    deserves a quick sanity check. We use Python's fingerprint of
    the same algorithm shape — exact equality of the **keys**
    rather than the hashes, since hashtext is server-side.
    """
    keys = {f"audit-chain-{_tenant_id(t)}" for t in range(_TENANTS_N)}
    assert len(keys) == _TENANTS_N, (
        f"per-tenant lock key collision in test fixture: only "
        f"{len(keys)} distinct keys for {_TENANTS_N} tenants"
    )


def test_run_ids_are_globally_unique_across_dimensions():
    """Sanity for the test fixture itself: the (t, p, u) tuple
    space generates 1000 globally unique run_ids. If the helper
    accidentally produced collisions, every concurrent finish()
    would race on the same ``workflow_runs.id`` and the assertion
    "1000 finish → 1000 billing rows" would silently pass with a
    smaller-than-expected number of rows.
    """
    ids = {
        _run_id(t, p, u)
        for t in range(_TENANTS_N)
        for p in range(_PROJECTS_PER_TENANT)
        for u in range(_USERS_PER_TENANT)
    }
    assert len(ids) == _TOTAL_RUNS, (
        f"run_id collision in fixture: {len(ids)} distinct ids "
        f"for {_TOTAL_RUNS} (t, p, u) tuples"
    )


