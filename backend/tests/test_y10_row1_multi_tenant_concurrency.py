@_requires_pg
@pytest.mark.asyncio
async def test_smaller_scale_concurrency_smoke(_y10_row1_db):
    """A 3 × 3 × 3 = 27-task smoke version of the same fan-out so
    a CI runner with a slow PG can still rule out regressions
    quickly without the full 30-60 s budget of the 1000-task case.

    If the 1000-task storm trips a transient flake (e.g. asyncpg
    pool exhaustion under cold-start), this 27-task variant still
    catches the structural deadlock / cross-talk regression. The
    dimensions stay in proportion (each tenant has both multiple
    projects AND multiple users) so the contextvar isolation +
    per-tenant lock contract is still exercised.
    """
    from backend import audit

    pool = _y10_row1_db

    # Seed a smaller fixture inline (don't reuse the full helper —
    # it always seeds 10 × 20 × 5).
    smoke_tenants = [f"{_TENANT_PREFIX}-smoke-{t:02d}" for t in range(3)]
    async with pool.acquire() as conn:
        async with conn.transaction():
            for t_idx, tid in enumerate(smoke_tenants):
                await conn.execute(
                    "INSERT INTO tenants (id, name, plan) "
                    "VALUES ($1, $2, 'free') "
                    "ON CONFLICT (id) DO NOTHING",
                    tid, f"Y10R1-smoke-{t_idx}",
                )
                for p_idx in range(3):
                    pid = f"p-y10r1-smoke-{t_idx}-{p_idx}"
                    await conn.execute(
                        "INSERT INTO projects "
                        "(id, tenant_id, product_line, name, slug) "
                        "VALUES ($1, $2, 'embedded', $3, $4) "
                        "ON CONFLICT (tenant_id, product_line, slug) "
                        "DO NOTHING",
                        pid, tid, f"smoke-{t_idx}-{p_idx}",
                        f"smoke-{t_idx}-{p_idx}",
                    )

    async def _smoke_task(tid: str, pid: str, u: int) -> str:
        from backend.db_context import set_project_id, set_tenant_id
        from backend.db_pool import get_pool
        from backend import workflow

        run_id = f"wf-{pid}-u{u}"
        set_tenant_id(tid)
        set_project_id(pid)
        try:
            async with get_pool().acquire() as conn:
                await conn.execute(
                    "INSERT INTO workflow_runs "
                    "(id, kind, started_at, status, metadata, "
                    " tenant_id, project_id) "
                    "VALUES ($1, 'invoke', $2, 'running', '{}', $3, $4)",
                    run_id, time.time(), tid, pid,
                )
            await audit.log(
                "workflow.started", "workflow_run", run_id,
                after={"smoke": True}, actor=f"smoke-u{u}@y10r1.local",
            )
            await workflow.finish(run_id, status="completed")
        finally:
            set_tenant_id(None)
            set_project_id(None)
        return run_id

    coros = []
    for t_idx, tid in enumerate(smoke_tenants):
        for p_idx in range(3):
            pid = f"p-y10r1-smoke-{t_idx}-{p_idx}"
            for u in range(3):
                coros.append(_smoke_task(tid, pid, u))
    random.shuffle(coros)
    run_ids = await asyncio.wait_for(
        asyncio.gather(*coros), timeout=60.0,
    )
    assert len(set(run_ids)) == 27

    # Each tenant's chain verifies; each tenant's smoke runs all
    # finished completed.
    results = await audit.verify_all_chains()
    for tid in smoke_tenants:
        ok, bad = results.get(tid, (False, None))
        assert ok, f"smoke tenant {tid} chain broken at {bad}"

    async with pool.acquire() as conn:
        n_completed = await conn.fetchval(
            "SELECT COUNT(*) FROM workflow_runs "
            "WHERE id LIKE 'wf-p-y10r1-smoke-%' AND status = 'completed'"
        )
        n_billing = await conn.fetchval(
            "SELECT COUNT(*) FROM billing_usage_events "
            "WHERE kind = 'workflow_run' "
            "  AND workflow_run_id LIKE 'wf-p-y10r1-smoke-%'"
        )
    assert n_completed == 27
    assert n_billing == 27

    # Cleanup the smoke-only rows (regular fixture TRUNCATE only
    # cleans the LIKE 'y10r1-%' pattern by tenant_id, which we hit;
    # but the projects table cleanup is via tenant_id LIKE so the
    # smoke projects above DO get cleaned by the fixture teardown).
