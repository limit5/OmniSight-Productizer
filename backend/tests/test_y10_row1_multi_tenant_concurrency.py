async def _run_one_workflow_task(
    *, t: int, p: int, u: int,
) -> tuple[str, str, str, str]:
    """One task in the concurrent fan-out.

    Returns ``(run_id, tenant_id, project_id, actor)`` so the
    outer ``asyncio.gather`` can cross-check the result vector
    against the expected attribution table without a second DB
    round-trip.

    Why each task does the full {set_contextvar → insert run →
    audit.log → workflow.finish → reset_contextvar} dance:

    * **set_contextvar** — ``audit.log`` reads ``tenant_id`` from
      ``backend.db_context.current_tenant_id()``; without setting
      it, the audit row would land in ``t-default`` and the
      per-tenant chain assertion would fail.
    * **insert run** — ``workflow.start`` does NOT take an explicit
      ``project_id``; we insert the row directly with the (tenant,
      project) tuple we want to attribute to.
    * **audit.log** — exercises the per-tenant advisory-lock path
      under cross-tenant concurrency (the lock key contains
      ``tenant_id``; cross-tenant tasks must NOT block each other).
    * **workflow.finish** — runs ``UPDATE workflow_runs`` with
      optimistic-lock + the LEFT JOIN read of (tenant, project,
      product_line) + the synchronous fan-out into
      ``billing_usage_events``. This is the load-bearing
      attribution path the Y10 row 1 acceptance hinges on.
    * **reset_contextvar** — finally-block guarantee that a task
      crashing mid-flight cannot leak its tenant slot to a future
      task on the same asyncio loop.
    """
    from backend import audit, workflow
    from backend.db_context import set_project_id, set_tenant_id
    from backend.db_pool import get_pool

    tid = _tenant_id(t)
    pid = _project_id(t, p)
    uid = _user_id(t, u)
    email = _user_email(t, u)
    run_id = _run_id(t, p, u)

    set_tenant_id(tid)
    set_project_id(pid)
    try:
        # Insert the workflow_runs row directly so we own the (t, p)
        # attribution — workflow.start does not write project_id.
        async with get_pool().acquire() as conn:
            await conn.execute(
                "INSERT INTO workflow_runs "
                "(id, kind, started_at, status, metadata, "
                " tenant_id, project_id) "
                "VALUES ($1, 'invoke', $2, 'running', '{}', $3, $4)",
                run_id, time.time(), tid, pid,
            )

        # Per-tenant audit chain write — exercises the per-tenant
        # advisory lock under cross-tenant concurrency.
        await audit.log(
            "workflow.started", "workflow_run", run_id,
            after={"user_id": uid, "project_id": pid},
            actor=email,
        )

        # Drive the load-bearing attribution path.
        await workflow.finish(run_id, status="completed")
    finally:
        set_tenant_id(None)
        set_project_id(None)

    return run_id, tid, pid, email


@_requires_pg
@pytest.mark.asyncio
async def test_concurrent_runs_no_deadlock_within_timeout(_y10_row1_db):
    """Drive 1000 concurrent ``(workflow_run, audit.log,
    workflow.finish)`` triples through ``asyncio.gather`` and assert
    the whole storm completes inside a generous 180 s wall clock.

    This is the deadlock guard. If a future refactor accidentally
    promoted the per-tenant audit advisory lock to a global key
    (or shared a lock between two tenants whose hashtext collided
    after a hash-function swap), this test would hang here and the
    timeout would trip.

    180 s is generous: with ``pg_test_pool`` capped at ``max_size=5``
    the 1000 tasks queue through 5 connections; per-tenant the 100
    audit writes serialise on the per-tenant advisory lock. Locally
    on PG 16 the full storm finishes in 30-60 s; in CI the budget
    is 3× to absorb cold-start variance.
    """
    pool = _y10_row1_db
    await _seed_full_y10_row1_fixture(pool)

    # Build the 1000-task vector + shuffle so the asyncio scheduler
    # cannot accidentally serialise tenant-by-tenant or project-by-
    # project.
    job_specs: list[tuple[int, int, int]] = []
    for t in range(_TENANTS_N):
        for p in range(_PROJECTS_PER_TENANT):
            for u in range(_USERS_PER_TENANT):
                job_specs.append((t, p, u))
