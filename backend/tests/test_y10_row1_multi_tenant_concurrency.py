"""Y10 #286 row 1 — multi-tenant + multi-project + multi-user concurrency
stress acceptance test.

Acceptance criterion (TODO §Y10 row 1)::

    多租戶 + 多 project + 多 user 並發壓力：10 tenant × 20 project × 5
    user × 同時跑 workflow_run → 無 cross-talk / 無 deadlock / 無 quota
    mis-attribution.

Three failure modes the test rules out
──────────────────────────────────────
1. **Cross-talk** — a workflow_run started under ``(tenant_A,
   project_A, user_A)`` must NEVER produce a billing row attributed
   to ``(tenant_B, ...)`` or ``(tenant_A, project_B, ...)``. Root
   cause would be a leaked ``ContextVar`` between asyncio tasks, a
   shared module-global pulled out of context, or a connection pool
   release-time reset that doesn't clear per-tenant session state.
2. **Deadlock** — concurrent ``audit.log`` writes serialise on
   ``pg_advisory_xact_lock(hashtext('audit-chain-' || tenant_id))``;
   that lock is per-tenant by design so cross-tenant writes must NOT
   block each other. If a refactor accidentally promoted the key to
   a global string (or shared a key between tenants), the test would
   hang on its 120 s wait_for guard.
3. **Quota mis-attribution** — every ``workflow_run`` finishing must
   produce exactly one ``billing_usage_events`` row carrying the
   correct ``(tenant_id, project_id)`` tuple read straight off the
   ``workflow_runs`` row (not from a request-scope ContextVar that
   may have leaked, and not from a fall-through ``p-<suffix>-default``
   bucket when the run was started against an explicit project).

Test layout
───────────
* **Block A — pure-unit drift guards** (always run): lock the
  acceptance dimensions and the contracts that make the concurrent
  Block B tests load-bearing. If a future refactor promotes the
  audit chain advisory lock to a non-tenant key, or makes
  ``workflow.finish`` read tenant/project from the ContextVar
  instead of the DB row, Block A trips on every CI run including
  ones without a PG service.
* **Block B — PG-required acceptance** (skip without
  ``OMNI_TEST_PG_URL``): drive the actual 10 × 20 × 5 = 1000 task
  fan-out and assert each of the three failure modes is observably
  absent in the resulting ``audit_log`` / ``workflow_runs`` /
  ``billing_usage_events`` triple.

Same skip-pattern as ``test_y9_row5_audit_billing_alignment.py`` so
the test lane gating stays consistent across the Y rows.

Module-global state audit (per implement_phase_step.md Step 1)
────────────────────────────────────────────────────────────────
This row is pure test code — zero new prod code, zero new
module-globals. The fixtures reset
``backend.db_context.set_tenant_id`` / ``set_project_id`` on
teardown so the heavy concurrent fan-out cannot leak into a
following test. The Prometheus cardinality cap counters in
``backend.metrics_labels`` are intentionally per-worker (audit
answer #3) and the test's 200 distinct ``project_id`` values stay
well under the 10 000 default cap, so no ``"other"`` bucket
collapse occurs and the per-row attribution assertions remain
sharp.

Read-after-write timing audit (per implement_phase_step.md Step 1)
────────────────────────────────────────────────────────────────
Each task ``await``s its writers (``audit.log`` /
``workflow.finish``) sequentially before the outer
``asyncio.gather`` returns; the assertions then read against the
fully-quiesced state. The PG advisory lock keyed on
``hashtext('audit-chain-' || tenant_id)`` ensures within-tenant
ordering, while the per-row ``BIGINT GENERATED BY DEFAULT AS
IDENTITY`` on ``billing_usage_events`` (alembic 0039) guarantees
that 1000 concurrent inserters cannot collide on row id —
cross-tenant ordering between rows is observable-time only and not
asserted on (the tests check counts and per-row attribution, not
ordering between unrelated tenants).
"""

