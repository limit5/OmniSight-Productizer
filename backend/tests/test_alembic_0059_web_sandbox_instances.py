"""W14.10 — alembic 0059 ``web_sandbox_instances`` migration contract.

Locks the load-bearing properties of the W14.10 table-add:

1.  **Structural** — revision ``0059`` chains onto ``0057`` (the
    AS.2.2 ``oauth_tokens`` row is the latest leaf in the auth/user
    chain; appending here keeps that lineage stable and leaves the
    AB-track chain ``0058 → 0181 → 0183 / 0184 → 0185`` untouched);
    the PG branch uses ``DOUBLE PRECISION`` + ``BIGINT GENERATED BY
    DEFAULT AS IDENTITY``; the SQLite branch uses ``REAL`` +
    ``INTEGER PRIMARY KEY AUTOINCREMENT`` and is ``IF NOT EXISTS``-
    guarded for idempotency.

2.  **Functional (SQLite)** — run ``upgrade()`` against an empty DB,
    then assert:
      * the ``web_sandbox_instances`` table exists,
      * every column the W14.10 row spec enumerates is present
        (``sandbox_id`` / ``workspace_id`` / ``started_at`` /
        ``ingress_url`` / ``status`` / ``last_request_at`` /
        ``killed_at`` / ``killed_reason``) plus the bookkeeping
        triplet (``created_at`` / ``updated_at`` / ``version``)
        and the ``id`` IDENTITY,
      * the partial UNIQUE on ``workspace_id`` WHERE status NOT IN
        ('stopped','failed') enforces the cross-worker invariant
        ("at most one live sidecar per workspace") — a second live
        INSERT for the same workspace_id must raise,
      * the same partial UNIQUE permits a fresh live row alongside
        a *terminal* row for the same workspace_id (audit-history
        pattern),
      * the ``status`` CHECK rejects an unknown status string and
        accepts every :class:`backend.web_sandbox.WebSandboxStatus`
        value verbatim,
      * the secondary ``idx_web_sandbox_instances_idle`` and
        ``idx_web_sandbox_instances_sandbox`` indexes were created
        and shape the W14.5 idle reaper / W14.3 ingress lookups.

3.  **Idempotency** — running ``upgrade()`` twice is a no-op:
    ``CREATE TABLE IF NOT EXISTS`` + ``CREATE INDEX IF NOT EXISTS``
    short-circuit on the second pass.

4.  **PG dialect branch** — capture SQL via a stub bind, verify the
    CREATE TABLE uses ``DOUBLE PRECISION`` (not ``REAL``) +
    ``BIGINT GENERATED BY DEFAULT AS IDENTITY`` (not
    ``AUTOINCREMENT``).  The PG-specific CHECK / partial UNIQUE
    syntax matches what the asyncpg + psycopg2 baseline accepts.

5.  **Cross-module drift guard** — the ``status`` CHECK clause in
    the migration source MUST byte-equal the sorted list of
    :class:`backend.web_sandbox.WebSandboxStatus` ``.value`` strings.
    Adding a new status must touch both the enum and this CHECK in
    the same PR.

6.  **Migrator coverage** — belt+braces the broader
    ``test_migrator_schema_coverage`` drift gate by pinning the
    expected entry name + INTEGER IDENTITY membership:
    ``web_sandbox_instances`` MUST be in ``TABLES_IN_ORDER`` AND in
    ``TABLES_WITH_IDENTITY_ID`` (since the PK is INTEGER IDENTITY,
    the sequence must restart after replay).

The schema half of the W14.10 contract; the launcher half (swap
:class:`backend.web_sandbox.WebSandboxManager` from in-memory cache
to PG-backed reads) lands in a separate row.
"""
