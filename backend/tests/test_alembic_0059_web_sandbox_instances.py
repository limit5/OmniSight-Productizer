class TestPgBranchExecutes:
    def test_pg_branch_emits_create_table_and_three_indexes(
        self, monkeypatch, m0059,
    ) -> None:
        from alembic import op as alembic_op

        captured: list[str] = []

        class _PgBind:
            class _Dialect:
                name = "postgresql"

            dialect = _Dialect()

            def exec_driver_sql(self, sql, *a, **k):
                captured.append(sql)

        monkeypatch.setattr(alembic_op, "get_bind", lambda: _PgBind())
        m0059.upgrade()

        # CREATE TABLE + three CREATE INDEX = exactly four statements.
        assert len(captured) == 4
        joined = "\n".join(captured)
        assert (
            "CREATE TABLE IF NOT EXISTS web_sandbox_instances" in joined
        )
        assert "DOUBLE PRECISION" in joined
        assert "BIGINT GENERATED BY DEFAULT AS IDENTITY" in joined
        assert (
            "idx_web_sandbox_instances_workspace_live" in joined
        )
        assert "WHERE status NOT IN ('stopped', 'failed')" in joined
        assert "idx_web_sandbox_instances_idle" in joined
        assert "idx_web_sandbox_instances_sandbox" in joined

    def test_pg_downgrade_drops_indexes_then_table(
        self, monkeypatch, m0059,
    ) -> None:
        from alembic import op as alembic_op

        captured: list[str] = []

        class _PgBind:
            class _Dialect:
                name = "postgresql"

            dialect = _Dialect()

            def exec_driver_sql(self, sql, *a, **k):
                captured.append(sql)

        # ``op.execute`` is the alembic public API used in the
        # downgrade; route it to our bind capture too.
        def _exec(sql):
            captured.append(str(sql))

        monkeypatch.setattr(alembic_op, "get_bind", lambda: _PgBind())
        monkeypatch.setattr(alembic_op, "execute", _exec)
        m0059.downgrade()
        joined = "\n".join(captured)
        assert (
            "DROP INDEX IF EXISTS idx_web_sandbox_instances_sandbox"
            in joined
        )
        assert (
            "DROP INDEX IF EXISTS idx_web_sandbox_instances_idle"
            in joined
        )
        assert (
            "DROP INDEX IF EXISTS "
            "idx_web_sandbox_instances_workspace_live"
            in joined
        )
        assert (
            "DROP TABLE IF EXISTS web_sandbox_instances" in joined
        )
        # All three index drops must precede the table drop —
        # explicit ordering documents intent (PG would let DROP
        # TABLE cascade-drop the indexes, but the staged drop is
        # easier to reason about during a partial-rollback recovery).
        i_tab = joined.find("DROP TABLE IF EXISTS")
        for idx_name in (
            "idx_web_sandbox_instances_sandbox",
            "idx_web_sandbox_instances_idle",
            "idx_web_sandbox_instances_workspace_live",
        ):
            i_idx = joined.find(f"DROP INDEX IF EXISTS {idx_name}")
            assert 0 <= i_idx < i_tab, (
                f"{idx_name} drop must precede table drop"
            )


# ─── Group 5: migrator drift guard ───────────────────────────────────────


class TestMigratorListsTable:
    """The Phase-3 G4 lesson: any new table MUST be added to the
    migrator's ``TABLES_IN_ORDER`` or the next prod cutover loses
    its data silently.  Plus: any INTEGER-PK table MUST be added to
    ``TABLES_WITH_IDENTITY_ID`` or the post-replay sequence-restart
    skips it and the next live INSERT collides with a preserved id.
    This class belt+braces the broader ``test_migrator_schema_coverage``
    drift gate by pinning both expected memberships."""

    @staticmethod
    def _load_migrator():
        import importlib.util as _u

        repo_root = Path(__file__).resolve().parents[2]
        spec = _u.spec_from_file_location(
            "migrate_sqlite_to_pg",
            repo_root / "scripts" / "migrate_sqlite_to_pg.py",
        )
        mig = _u.module_from_spec(spec)
        sys.modules["migrate_sqlite_to_pg"] = mig
        spec.loader.exec_module(mig)
        return mig

    def test_web_sandbox_instances_in_tables_in_order(self) -> None:
        mig = self._load_migrator()
        assert "web_sandbox_instances" in mig.TABLES_IN_ORDER

    def test_web_sandbox_instances_in_identity_id_set(self) -> None:
        # PK is BIGINT GENERATED BY DEFAULT AS IDENTITY on PG /
        # INTEGER PRIMARY KEY AUTOINCREMENT on SQLite — the sequence
        # must restart after replay so the next live insert doesn't
        # collide with a preserved id.
        mig = self._load_migrator()
        assert "web_sandbox_instances" in mig.TABLES_WITH_IDENTITY_ID

    def test_web_sandbox_instances_no_fk_so_position_is_late(self) -> None:
        # No FK to any other table; placement near the end with the
        # other operational / audit tables (after billing_usage_events
        # / catalog_entries / install_jobs / catalog_subscriptions)
        # is the row's documented intent.
        mig = self._load_migrator()
        order = list(mig.TABLES_IN_ORDER)
        idx = order.index("web_sandbox_instances")
        # Must be after the catalog tables (BS.1.1 lineage).
        assert idx > order.index("catalog_entries")
        assert idx > order.index("install_jobs")
        assert idx > order.index("catalog_subscriptions")
