@pytest.fixture(scope="module")
def requirements_txt() -> str:
    return (REPO_ROOT / "backend" / "requirements.txt").read_text(encoding="utf-8")


def test_requirements_txt_is_autogenerated_lockfile(requirements_txt: str) -> None:
    head = requirements_txt.splitlines()[:6]
    joined = "\n".join(head)
    assert "autogenerated by pip-compile" in joined, (
        "backend/requirements.txt must be regenerated via `pip-compile --generate-hashes`"
    )
    assert "--generate-hashes" in joined, (
        "pip-compile invocation must include --generate-hashes in the header"
    )


def test_requirements_txt_has_hashes_on_every_pin(requirements_txt: str) -> None:
    # Every package line (ends with `==X.Y.Z \`) must have at least one
    # accompanying `--hash=sha256:...` token.
    pins = re.findall(r"^([a-zA-Z0-9._\[\]-]+)==[^\s]+\s*\\\s*$", requirements_txt, re.MULTILINE)
    assert pins, "no pins detected in requirements.txt (lockfile generation broken?)"
    hash_count = requirements_txt.count("--hash=sha256:")
    # Each pin should have >=1 hash; wheels + sdists usually give >=2.
    assert hash_count >= len(pins), (
        f"requirements.txt has {len(pins)} pins but only {hash_count} sha256 hashes"
    )


