"""N1 — Dependency governance guards.

These tests are the runtime counterpart to the `lockfile-drift` CI job.
They run fast (pure filesystem/string inspection, no network) and fail
loudly whenever the repo's hand-written invariants about lockfiles,
Node version pins, and Docker hash enforcement are broken.

What we check:

1. `package.json` pins `engines.node` / `engines.pnpm` and names
   `pnpm` as the package manager.
2. `.nvmrc` / `.node-version` both read `20.17.0` (kept in lockstep so
   nvm, fnm, asdf, Volta all pick up the same version).
3. Single-lockfile policy: `pnpm-lock.yaml` exists; `package-lock.json`
   and `yarn.lock` do NOT (they are also `.gitignore`d so a stray
   `npm install` can't accidentally re-commit one).
4. `backend/requirements.in` exists and is the source of truth.
5. `backend/requirements.txt` is a pip-tools hash-lockfile (autogenerated
   header + `--hash=sha256:...` tokens on every pin).
6. `Dockerfile.backend` installs with `--require-hashes`.
7. CI workflow contains the `lockfile-drift` job.
"""

from __future__ import annotations

import json
import re
from pathlib import Path

import pytest

REPO_ROOT = Path(__file__).resolve().parents[2]


# ---------------------------------------------------------------------------
# package.json engines + packageManager
# ---------------------------------------------------------------------------

@pytest.fixture(scope="module")
def package_json() -> dict:
    return json.loads((REPO_ROOT / "package.json").read_text(encoding="utf-8"))


def test_package_json_engines_pins_node_20(package_json: dict) -> None:
    engines = package_json.get("engines")
    assert engines, "package.json must declare engines (N1)"
    node_range = engines.get("node", "")
    # Must pin to 20.17.0+ and cap before the next major.
    assert ">=20.17.0" in node_range, f"node engine must require >=20.17.0, got {node_range!r}"
    assert "<21" in node_range, f"node engine must cap <21, got {node_range!r}"


def test_package_json_engines_pins_pnpm(package_json: dict) -> None:
    pnpm_range = package_json.get("engines", {}).get("pnpm", "")
    assert pnpm_range.startswith(">=9"), f"pnpm engine must require >=9, got {pnpm_range!r}"


def test_package_json_declares_pnpm_as_package_manager(package_json: dict) -> None:
    pm = package_json.get("packageManager", "")
    assert pm.startswith("pnpm@"), (
        f"packageManager must be pnpm (N1 single-lockfile policy), got {pm!r}"
    )


# ---------------------------------------------------------------------------
# Node version files kept in lockstep
# ---------------------------------------------------------------------------

def test_nvmrc_and_node_version_match() -> None:
    nvmrc = (REPO_ROOT / ".nvmrc").read_text(encoding="utf-8").strip()
    nodever = (REPO_ROOT / ".node-version").read_text(encoding="utf-8").strip()
    assert nvmrc == "20.17.0", f".nvmrc must be 20.17.0, got {nvmrc!r}"
    assert nodever == "20.17.0", f".node-version must be 20.17.0, got {nodever!r}"
    assert nvmrc == nodever, "nvmrc and node-version must match"


# ---------------------------------------------------------------------------
# Single-lockfile policy
# ---------------------------------------------------------------------------

def test_pnpm_lock_exists() -> None:
    assert (REPO_ROOT / "pnpm-lock.yaml").is_file(), (
        "pnpm-lock.yaml is the canonical JS lockfile (N1) — it must be committed"
    )


def test_no_stray_js_lockfiles() -> None:
    # These lockfiles are explicitly forbidden under the single-lockfile
    # policy. The CI job `lockfile-drift` enforces the same rule.
    stray = [f for f in ("package-lock.json", "yarn.lock") if (REPO_ROOT / f).exists()]
    assert not stray, f"Stray lockfile(s) committed: {stray} — delete them (N1 policy)"


def test_gitignore_blocks_stray_lockfiles() -> None:
    gi = (REPO_ROOT / ".gitignore").read_text(encoding="utf-8")
    assert "package-lock.json" in gi, ".gitignore must block package-lock.json (N1)"
    assert "yarn.lock" in gi, ".gitignore must block yarn.lock (N1)"


# ---------------------------------------------------------------------------
# Python hash-locked requirements
# ---------------------------------------------------------------------------

def test_requirements_in_exists() -> None:
    assert (REPO_ROOT / "backend" / "requirements.in").is_file(), (
        "backend/requirements.in is the source of truth for Python deps (N1)"
    )


@pytest.fixture(scope="module")
def requirements_txt() -> str:
    return (REPO_ROOT / "backend" / "requirements.txt").read_text(encoding="utf-8")


def test_requirements_txt_is_autogenerated_lockfile(requirements_txt: str) -> None:
    head = requirements_txt.splitlines()[:6]
    joined = "\n".join(head)
    assert "autogenerated by pip-compile" in joined, (
        "backend/requirements.txt must be regenerated via `pip-compile --generate-hashes`"
    )
    assert "--generate-hashes" in joined, (
        "pip-compile invocation must include --generate-hashes in the header"
    )


def test_requirements_txt_has_hashes_on_every_pin(requirements_txt: str) -> None:
    # Every package line (ends with `==X.Y.Z \`) must have at least one
    # accompanying `--hash=sha256:...` token.
    pins = re.findall(r"^([a-zA-Z0-9._\[\]-]+)==[^\s]+\s*\\\s*$", requirements_txt, re.MULTILINE)
    assert pins, "no pins detected in requirements.txt (lockfile generation broken?)"
    hash_count = requirements_txt.count("--hash=sha256:")
    # Each pin should have >=1 hash; wheels + sdists usually give >=2.
    assert hash_count >= len(pins), (
        f"requirements.txt has {len(pins)} pins but only {hash_count} sha256 hashes"
    )


# ---------------------------------------------------------------------------
# Docker + CI wiring
# ---------------------------------------------------------------------------

def test_dockerfile_backend_uses_require_hashes() -> None:
    dockerfile = (REPO_ROOT / "Dockerfile.backend").read_text(encoding="utf-8")
    assert "--require-hashes" in dockerfile, (
        "Dockerfile.backend must `pip install --require-hashes -r requirements.txt` (N1)"
    )


def test_ci_has_lockfile_drift_job() -> None:
    ci = (REPO_ROOT / ".github" / "workflows" / "ci.yml").read_text(encoding="utf-8")
    assert "lockfile-drift:" in ci, (
        "CI workflow must define a `lockfile-drift` job (N1)"
    )
    # The drift job must actually regenerate and diff the Python lock.
    assert "pip-compile" in ci, "lockfile-drift job must re-run pip-compile"
    assert "--frozen-lockfile" in ci, "lockfile-drift job must enforce pnpm frozen lockfile"
