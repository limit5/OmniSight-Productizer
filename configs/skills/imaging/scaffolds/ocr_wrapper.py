from __future__ import annotations

import subprocess
from dataclasses import dataclass
from enum import Enum
from pathlib import Path
from typing import Any


class OCREngineType(str, Enum):
    TESSERACT = "tesseract"
    PADDLEOCR = "paddleocr"
    VENDOR_SDK = "vendor_sdk"


@dataclass
class OCRRegion:
    text: str
    confidence: float
    x: int
    y: int
    width: int
    height: int
    line: int


@dataclass
class OCRPageResult:
    engine: str
    language: str
    text: str
    regions: list[OCRRegion]
    confidence: float
    elapsed_ms: float


def ocr_tesseract(image_path: str, language: str = "eng") -> OCRPageResult:
    """Run Tesseract OCR on a single image."""
    result = subprocess.run(
        ["tesseract", image_path, "stdout", "-l", language, "--oem", "1"],
        capture_output=True, text=True, timeout=60,
    )
    return OCRPageResult(
        engine="tesseract",
        language=language,
        text=result.stdout.strip(),
        regions=[],
        confidence=0.0,
        elapsed_ms=0.0,
    )


def ocr_paddleocr(image_path: str, language: str = "ch") -> OCRPageResult:
    """Run PaddleOCR on a single image."""
    try:
        from paddleocr import PaddleOCR
        ocr = PaddleOCR(use_angle_cls=True, lang=language)
        result = ocr.ocr(image_path, cls=True)
        text_lines = []
        regions = []
        for line_data in (result[0] or []):
            box, (text, conf) = line_data
            text_lines.append(text)
            regions.append(OCRRegion(
                text=text, confidence=conf,
                x=int(box[0][0]), y=int(box[0][1]),
                width=int(box[2][0] - box[0][0]),
                height=int(box[2][1] - box[0][1]),
                line=len(regions),
            ))
        return OCRPageResult(
            engine="paddleocr",
            language=language,
            text="\n".join(text_lines),
            regions=regions,
            confidence=sum(r.confidence for r in regions) / max(len(regions), 1),
            elapsed_ms=0.0,
        )
    except ImportError:
        raise RuntimeError("PaddleOCR not installed: pip install paddleocr paddlepaddle")
