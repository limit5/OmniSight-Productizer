"""G4 #4 (HA-04) — SQLite → PostgreSQL data migration with audit_log hash chain verification.

Migrates every table in the OmniSight SQLite database to an empty
PostgreSQL database created by ``alembic upgrade head`` against the
same revision (head = 0015 at time of writing). The schema is *not*
touched by this script: Alembic owns DDL, this owns DML.

Why this is its own script and not "just dump + restore":

1.  **Hash-chain preservation is the whole point.** ``audit_log``
    rows carry a per-tenant Merkle-style chain
    (``curr_hash = sha256(prev_hash || canonical(row))``). Any reorder,
    row drop, or float-to-float rewrite of ``ts`` would silently shatter
    tamper-evidence for every downstream row. Off-the-shelf tools
    (``pgloader``, ``sqlite3-to-postgres`` PyPI packages) reorder rows
    by primary key and rewrite types without asking. We walk rows in
    ``ORDER BY id ASC`` and insert them with their exact original id,
    ts, prev_hash, and curr_hash, then re-verify the chain on the
    *target* to prove nothing drifted. See ``verify_hash_chain_in_rows``.

2.  **IDENTITY sequence alignment.** SQLite's ``INTEGER PRIMARY KEY``
    becomes PG ``BIGINT GENERATED BY DEFAULT AS IDENTITY`` via the
    G4 #1 compat shim. Inserting rows with explicit ids does *not*
    advance PG's identity sequence — the next implicit insert would
    collide with id=1. Every migrated table with an auto-id column
    gets ``setval(pg_get_serial_sequence(...), max(id))`` at the end.

3.  **Fail-closed invariants.** Any of the following aborts with a
    non-zero exit code and leaves the target untouched (or at worst
    in the post-``TRUNCATE`` empty state):
      - source audit_log chain broken before we start (exit 3)
      - target has existing non-trivial data and ``--truncate-target``
        was not passed (exit 4)
      - target audit_log chain broken after copy (exit 5 — implies a
        migration bug, never expected)
      - row count mismatch between source and target (exit 6)

Usage::

    # Default: fail if target has any data beyond the Alembic seed row
    python3 scripts/migrate_sqlite_to_pg.py \\
        --source   sqlite:///data/omnisight.db \\
        --target   postgresql+asyncpg://omni:pw@pg-primary:5432/omnisight

    # Wipe the target first (still preserves schema)
    python3 scripts/migrate_sqlite_to_pg.py \\
        --source  data/omnisight.db \\
        --target  postgresql+asyncpg://omni:pw@localhost:5432/omnisight \\
        --truncate-target

    # Dry run: walk every row, compute chain, verify source only.
    python3 scripts/migrate_sqlite_to_pg.py \\
        --source  data/omnisight.db --dry-run

    # Structured output for CI
    python3 scripts/migrate_sqlite_to_pg.py --source ... --target ... --json

Exit codes::

    0  migration completed, hash chain verified on both sides
    1  unexpected error / driver unavailable / connection failure
    2  CLI usage error
    3  source audit_log chain broken BEFORE migration started
    4  target not empty and --truncate-target not set
    5  target audit_log chain broken AFTER migration (corruption bug)
    6  row count mismatch between source and target after copy

This script deliberately keeps its core logic as pure functions so the
contract test suite can exercise the hash-chain walk, insert-SQL
builder, and argparse surface without requiring a real Postgres.
"""

