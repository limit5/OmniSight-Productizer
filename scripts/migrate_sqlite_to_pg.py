def build_insert_sql(
    table: str,
    columns: Sequence[str],
    *,
    on_conflict: str | None = None,
) -> str:
    """Return a parameterised ``INSERT INTO ...`` statement for asyncpg.

    Placeholders are ``$1, $2, ...`` — asyncpg's native paramstyle.
    ``on_conflict`` lets callers append an ``ON CONFLICT ...`` clause
    (we use ``DO NOTHING`` for the idempotent ``t-default`` tenants
    seed row).
    """
    if not columns:
        raise ValueError(f"no columns provided for {table}")
    col_list = ", ".join(f'"{c}"' for c in columns)
    placeholders = ", ".join(f"${i + 1}" for i in range(len(columns)))
    sql = f'INSERT INTO "{table}" ({col_list}) VALUES ({placeholders})'
    if on_conflict:
        sql += f" {on_conflict}"
    return sql


def build_sequence_reset_sql(table: str, column: str = "id") -> str:
    """Return a ``SELECT setval(...)`` that re-aligns an IDENTITY column.

    We use ``pg_get_serial_sequence`` so this works uniformly for
    IDENTITY (PG 10+) and legacy SERIAL columns.
    """
    return (
        f"SELECT setval("
        f"pg_get_serial_sequence('\"{table}\"', '{column}'), "
        f"COALESCE((SELECT MAX(\"{column}\") FROM \"{table}\"), 1), "
        f"(SELECT MAX(\"{column}\") IS NOT NULL FROM \"{table}\"))"
    )


