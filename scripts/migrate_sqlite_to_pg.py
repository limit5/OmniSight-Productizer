# ─────────────────────────────────────────────────────────────────────────
#  Hash chain — mirrored from backend/audit.py so this script does not
#  depend on the async audit module (which pulls in db_context + config).
# ─────────────────────────────────────────────────────────────────────────


def canonical_payload(row: Mapping[str, Any]) -> dict[str, Any]:
    """Return the payload dict that the audit chain hashes.

    The shape matches :func:`backend.audit.log`:

    ``{action, entity_kind, entity_id, before, after, actor}``

    ``before_json`` / ``after_json`` store compact JSON; we parse them
    so the re-hash matches the original (audit.log serialises with
    ``json.dumps(ensure_ascii=False)`` before hashing — feeding back
    the decoded dict gives us an identical canonical form).
    """
    return {
        "action":      row["action"],
        "entity_kind": row["entity_kind"],
        "entity_id":   row["entity_id"] or "",
        "before":      json.loads(row["before_json"] or "{}"),
        "after":       json.loads(row["after_json"] or "{}"),
        "actor":       row["actor"],
    }


def canonical_json(payload: Mapping[str, Any]) -> str:
    """Deterministic JSON encoding used inside the hash input."""
    return json.dumps(
        payload,
        sort_keys=True,
        separators=(",", ":"),
        ensure_ascii=False,
        default=str,
    )


def recompute_hash(prev_hash: str, row: Mapping[str, Any]) -> str:
    """Return the ``curr_hash`` this row SHOULD have given ``prev_hash``.

    Mirrors :func:`backend.audit._hash` plus the ``ts`` round trick used
    by :func:`backend.audit.log`::

        curr = sha256(prev_hash + canonical(payload) + str(round(ts, 6)))
    """
    payload = canonical_payload(row)
    seed = prev_hash + canonical_json(payload) + str(round(row["ts"], 6))
    return hashlib.sha256(seed.encode("utf-8")).hexdigest()


@dataclass(frozen=True)
class ChainBreak:
    """The first audit row where the chain diverges."""

    tenant_id:    str
    row_id:       int
    why:          str            # "prev_hash" | "curr_hash"
    expected:     str
    actual:       str


def verify_hash_chain_in_rows(
    rows: Sequence[Mapping[str, Any]],
) -> tuple[bool, ChainBreak | None]:
    """Walk rows assumed to be one tenant, ordered by id ascending.

    Returns ``(True, None)`` if the chain holds, else
    ``(False, ChainBreak)`` pinned to the first divergent row.

    This is the pure, connection-free version of
    :func:`backend.audit.verify_chain` used by both the source
    pre-flight and the target post-flight checks.
    """
    prev_hash = ""
    for row in rows:
        expected_curr = recompute_hash(prev_hash, row)
        if row["prev_hash"] != prev_hash:
            return False, ChainBreak(
                tenant_id=row.get("tenant_id") or "",
                row_id=row["id"],
                why="prev_hash",
                expected=prev_hash,
                actual=row["prev_hash"] or "",
            )
        if row["curr_hash"] != expected_curr:
            return False, ChainBreak(
                tenant_id=row.get("tenant_id") or "",
                row_id=row["id"],
                why="curr_hash",
                expected=expected_curr,
                actual=row["curr_hash"] or "",
            )
        prev_hash = row["curr_hash"]
    return True, None


# ─────────────────────────────────────────────────────────────────────────
#  SQL builders (pure).
