@dataclass
class TableResult:
    table:            str
    source_rows:      int = 0
    copied_rows:      int = 0
    skipped:          bool = False
    skipped_reason:   str = ""
    columns:          list[str] = field(default_factory=list)


@dataclass
class MigrationReport:
    source_url:               str = ""
    target_url:               str = ""
    dry_run:                  bool = False
    truncated_target:         bool = False
    source_chain_ok:          bool = False
    source_chain_tenants:     dict[str, dict[str, Any]] = field(default_factory=dict)
    target_chain_ok:          bool | None = None
    target_chain_tenants:     dict[str, dict[str, Any]] = field(default_factory=dict)
    tables:                   list[TableResult] = field(default_factory=list)
    sequences_reset:          dict[str, int] = field(default_factory=dict)
    duration_seconds:         float = 0.0
    exit_code:                int = 0

    def to_dict(self) -> dict[str, Any]:
        return {
            "source_url":           self.source_url,
            "target_url":           self.target_url,
            "dry_run":              self.dry_run,
            "truncated_target":     self.truncated_target,
            "source_chain_ok":      self.source_chain_ok,
            "source_chain_tenants": self.source_chain_tenants,
            "target_chain_ok":      self.target_chain_ok,
            "target_chain_tenants": self.target_chain_tenants,
            "tables": [
                {
                    "table":           t.table,
                    "source_rows":     t.source_rows,
                    "copied_rows":     t.copied_rows,
                    "skipped":         t.skipped,
                    "skipped_reason":  t.skipped_reason,
                    "columns":         t.columns,
                }
                for t in self.tables
            ],
            "sequences_reset":    self.sequences_reset,
            "duration_seconds":   round(self.duration_seconds, 3),
            "exit_code":          self.exit_code,
        }


# ─────────────────────────────────────────────────────────────────────────
#  Source (aiosqlite) helpers.
# ─────────────────────────────────────────────────────────────────────────


async def _sqlite_open(url: DatabaseURL) -> Any:  # returns aiosqlite.Connection
    import aiosqlite  # lazy

    path = url.sqlite_path()
    if not path.exists():
        raise FileNotFoundError(f"SQLite source not found: {path}")
    conn = await aiosqlite.connect(str(path))
    conn.row_factory = aiosqlite.Row
    return conn


async def list_source_tables(sqlite_conn: Any) -> list[str]:
    """Return names of all non-internal tables present on the source."""
    async with sqlite_conn.execute(
        "SELECT name FROM sqlite_master WHERE type='table' "
        "AND name NOT LIKE 'sqlite_%' AND name NOT LIKE 'alembic_%' "
        "ORDER BY name"
    ) as cur:
        rows = await cur.fetchall()
    return [r["name"] for r in rows]


async def list_source_columns(sqlite_conn: Any, table: str) -> list[str]:
    """Return column names for *table* in the order the source declared them."""
    # PRAGMA table_info returns (cid, name, type, notnull, dflt_value, pk);
    # ``row[1]`` is the column name regardless of row_factory.
    async with sqlite_conn.execute(f"PRAGMA table_info({table})") as cur:
        rows = await cur.fetchall()
    return [row[1] for row in rows]


async def count_source_rows(sqlite_conn: Any, table: str) -> int:
    async with sqlite_conn.execute(f'SELECT COUNT(*) FROM "{table}"') as cur:
        row = await cur.fetchone()
    return int(row[0]) if row else 0


async def fetch_source_rows(
    sqlite_conn: Any,
    table: str,
    columns: Sequence[str],
    *,
    order_by: str | None = None,
) -> list[dict[str, Any]]:
    """Fetch every row from *table* as a list of dicts.

    We load the whole table into memory for simplicity — OmniSight's
    ops scale is hundreds of thousands of rows for audit_log and a
    few hundred for everything else. If this ever becomes a concern
    (tens of millions of rows) we'll swap to a streaming cursor, but
    the pure-function invariants (hash chain walked in-order) stay
    the same.
    """
    col_list = ", ".join(f'"{c}"' for c in columns)
    sql = f'SELECT {col_list} FROM "{table}"'
    if order_by:
        sql += f" ORDER BY {order_by}"
    async with sqlite_conn.execute(sql) as cur:
        rows = await cur.fetchall()
    return [dict(r) for r in rows]


async def collect_audit_rows_by_tenant(
    sqlite_conn: Any,
) -> dict[str, list[dict[str, Any]]]:
    """Return ``{tenant_id: [rows ordered by id ASC, ...]}``.

    Used both as the pre-flight chain verifier source and as the
    canonical insert order for the target migration: preserving
    ORDER BY id ASC is what keeps the hash chain intact.
    """
    cols = await list_source_columns(sqlite_conn, "audit_log")
    if not cols:
        return {}
    rows = await fetch_source_rows(
        sqlite_conn, "audit_log", cols, order_by="id ASC"
    )
    grouped: dict[str, list[dict[str, Any]]] = {}
    for r in rows:
        tid = r.get("tenant_id") or DEFAULT_TENANT_ID
        grouped.setdefault(tid, []).append(r)
    return grouped


# ─────────────────────────────────────────────────────────────────────────
#  Target (asyncpg) helpers.
# ─────────────────────────────────────────────────────────────────────────


async def _pg_open(url: DatabaseURL) -> Any:  # returns asyncpg.Connection
    try:
        import asyncpg  # lazy
    except ImportError as exc:
        raise RuntimeError(
            "asyncpg is not installed. `pip install asyncpg` first."
        ) from exc
    kw = url.asyncpg_connect_kwargs()
    return await asyncpg.connect(**kw)


async def count_target_rows(pg_conn: Any, table: str) -> int:
    row = await pg_conn.fetchrow(f'SELECT COUNT(*) AS n FROM "{table}"')
    return int(row["n"]) if row else 0


async def target_table_exists(pg_conn: Any, table: str) -> bool:
    row = await pg_conn.fetchrow(
        "SELECT 1 FROM information_schema.tables "
        "WHERE table_schema = current_schema() AND table_name = $1",
        table,
    )
    return row is not None


async def truncate_target(pg_conn: Any, tables: Sequence[str]) -> list[str]:
    """TRUNCATE ... RESTART IDENTITY CASCADE on every given table.

    CASCADE handles the FK references into ``tenants``. We do a single
    TRUNCATE statement for atomicity (a fail half-way leaves a coherent
    empty set; mixing TRUNCATE with DELETE would not).
    """
    present = [t for t in tables if await target_table_exists(pg_conn, t)]
    if not present:
        return []
    quoted = ", ".join(f'"{t}"' for t in present)
    await pg_conn.execute(
        f"TRUNCATE TABLE {quoted} RESTART IDENTITY CASCADE"
    )
    return present


async def reset_identity_sequences(
    pg_conn: Any,
    tables: Iterable[str],
) -> dict[str, int]:
    """Advance the IDENTITY sequence for each table to MAX(id).

    Returns ``{table: new_sequence_value}``. Skips tables that don't
    exist on the target (e.g. if the operator filtered with --tables).
    """
    out: dict[str, int] = {}
    for table in tables:
        if not await target_table_exists(pg_conn, table):
            continue
        sql = build_sequence_reset_sql(table)
        row = await pg_conn.fetchrow(sql)
        if row is not None:
            val = int(list(row.values())[0]) if row else 0
            out[table] = val
    return out


async def copy_rows_to_target(
    pg_conn: Any,
    table: str,
    columns: Sequence[str],
    rows: Sequence[Mapping[str, Any]],
    *,
    batch_size: int,
    on_conflict: str | None = None,
) -> int:
    """executemany the rows in batches; return number inserted."""
    if not rows:
        return 0
    sql = build_insert_sql(table, columns, on_conflict=on_conflict)
    total = 0
    # Convert dict rows to ordered tuples matching *columns*.
    for start in range(0, len(rows), batch_size):
        batch = rows[start : start + batch_size]
        tuples = [tuple(r.get(c) for c in columns) for r in batch]
        await pg_conn.executemany(sql, tuples)
        total += len(batch)
    return total


# ─────────────────────────────────────────────────────────────────────────
#  Source / target pre-flight (pure except for DB reads).
# ─────────────────────────────────────────────────────────────────────────


async def verify_source_chain(
    sqlite_conn: Any,
) -> tuple[bool, dict[str, dict[str, Any]]]:
    """Walk every tenant's chain on the source.

    Returns ``(all_ok, per_tenant_report)``. The per-tenant report
    shape matches :meth:`MigrationReport.source_chain_tenants`:
    ``{tenant_id: {"ok": bool, "first_bad_id": int|None, "why": str}}``.
    """
    grouped = await collect_audit_rows_by_tenant(sqlite_conn)
    report: dict[str, dict[str, Any]] = {}
    all_ok = True
    for tid, rows in grouped.items():
        ok, brk = verify_hash_chain_in_rows(rows)
        if not ok:
            all_ok = False
            report[tid] = {
                "ok":           False,
                "first_bad_id": brk.row_id if brk else None,
                "why":          brk.why if brk else "",
                "expected":     brk.expected if brk else "",
                "actual":       brk.actual if brk else "",
                "rows":         len(rows),
            }
        else:
            report[tid] = {"ok": True, "first_bad_id": None, "rows": len(rows)}
    return all_ok, report


async def check_target_empty(
    pg_conn: Any,
    tables: Sequence[str],
) -> dict[str, int]:
    """Return ``{table: non_trivial_row_count}`` for tables that have
    data beyond the Alembic-seeded ``t-default`` row.

    An empty dict means the target is safe to migrate into without
    ``--truncate-target``.
    """
    dirty: dict[str, int] = {}
    for t in tables:
        if not await target_table_exists(pg_conn, t):
            continue
        n = await count_target_rows(pg_conn, t)
        # ``tenants`` is seeded with exactly one row (``t-default``)
        # by Alembic migration 0012, so one row there is fine.
        if t == "tenants":
            if n > 1:
                dirty[t] = n
        elif n > 0:
            dirty[t] = n
    return dirty


# ─────────────────────────────────────────────────────────────────────────
#  Post-flight: re-walk the chain on the target.
# ─────────────────────────────────────────────────────────────────────────


async def verify_target_chain(
    pg_conn: Any,
) -> tuple[bool, dict[str, dict[str, Any]]]:
    """Walk every tenant's chain on the freshly-migrated target.

    If this ever returns ``ok=False`` it is a migration bug, not a
    data bug — the source was verified before we started. Exit 5.
    """
    # Discover what tenants are present on target; reuse same pure
    # verifier as on the source by fetching all audit rows grouped.
    tenants_rows = await pg_conn.fetch(
        "SELECT DISTINCT tenant_id FROM audit_log ORDER BY tenant_id"
    )
    report: dict[str, dict[str, Any]] = {}
    all_ok = True
    for trow in tenants_rows:
        tid = trow["tenant_id"]
        rows = await pg_conn.fetch(
            "SELECT id, ts, actor, action, entity_kind, entity_id, "
            "before_json, after_json, prev_hash, curr_hash, tenant_id "
            "FROM audit_log WHERE tenant_id = $1 ORDER BY id ASC",
            tid,
        )
        row_dicts = [dict(r) for r in rows]
        ok, brk = verify_hash_chain_in_rows(row_dicts)
        if not ok:
            all_ok = False
            report[tid] = {
                "ok":           False,
                "first_bad_id": brk.row_id if brk else None,
                "why":          brk.why if brk else "",
                "rows":         len(row_dicts),
            }
        else:
            report[tid] = {"ok": True, "first_bad_id": None, "rows": len(row_dicts)}
    return all_ok, report


# ─────────────────────────────────────────────────────────────────────────
#  Orchestrator.
# ─────────────────────────────────────────────────────────────────────────


async def _run_migration(args: argparse.Namespace) -> MigrationReport:
    """Async driver; see :func:`main` for the sync CLI wrapper."""
    t0 = time.monotonic()
    report = MigrationReport(dry_run=bool(args.dry_run))

    # ── Parse URLs before touching any connection ──
    source_url = parse_source_arg(args.source)
    report.source_url = source_url.redacted()
    if not args.dry_run:
        target_url = parse_target_arg(args.target)
        report.target_url = target_url.redacted()
    else:
        target_url = None

    # Table list (intersect operator-provided filter with known order)
    selected: list[str]
    if args.tables:
        requested = [t.strip() for t in args.tables.split(",") if t.strip()]
        unknown = [t for t in requested if t not in TABLES_IN_ORDER]
        if unknown:
            raise ValueError(f"unknown tables: {unknown!r}")
        selected = [t for t in TABLES_IN_ORDER if t in set(requested)]
    else:
        selected = list(TABLES_IN_ORDER)

    sqlite_conn = await _sqlite_open(source_url)
    pg_conn: Any = None
    try:
        # Drop tables not actually present on source (source may be
        # older than current alembic head).
        present_on_source = set(await list_source_tables(sqlite_conn))
        effective = [t for t in selected if t in present_on_source]

        # ── Source chain pre-flight ──
        if not args.skip_chain_verify:
            ok, tenant_report = await verify_source_chain(sqlite_conn)
            report.source_chain_ok = ok
            report.source_chain_tenants = tenant_report
            if not ok:
                report.exit_code = 3
                return report
        else:
            report.source_chain_ok = True  # operator opted out

        if args.dry_run:
            # Still populate per-table source counts for visibility.
            for table in effective:
                cols = await list_source_columns(sqlite_conn, table)
                n = await count_source_rows(sqlite_conn, table)
                report.tables.append(TableResult(
                    table=table, source_rows=n, copied_rows=0,
                    skipped=True, skipped_reason="dry-run",
                    columns=cols,
                ))
            report.exit_code = 0
            return report

        # ── Open target ──
        pg_conn = await _pg_open(target_url)  # type: ignore[arg-type]

        # ── Target emptiness gate ──
        dirty = await check_target_empty(pg_conn, effective)
        if dirty and not args.truncate_target:
            logger.error(
                "target has existing data in %d table(s): %s. "
                "Pass --truncate-target to wipe or clear manually.",
                len(dirty), sorted(dirty),
            )
            report.exit_code = 4
            return report

        if args.truncate_target:
            truncated = await truncate_target(pg_conn, effective)
            report.truncated_target = bool(truncated)
            if not args.quiet:
                logger.info("truncated %d target tables", len(truncated))

        # ── Copy per-table ──
        for table in effective:
            cols = await list_source_columns(sqlite_conn, table)
            if not cols:
                report.tables.append(TableResult(
                    table=table, skipped=True,
                    skipped_reason="no columns on source",
                ))
                continue

            if not await target_table_exists(pg_conn, table):
                report.tables.append(TableResult(
                    table=table, columns=cols, skipped=True,
                    skipped_reason="target table does not exist "
                                  "(run `alembic upgrade head` first)",
                ))
                continue

            # audit_log MUST be migrated in id order across the whole
            # table (not per-tenant) to match SQLite's insertion order.
            order_by = "id ASC" if "id" in cols else None
            rows = await fetch_source_rows(
                sqlite_conn, table, cols, order_by=order_by
            )

            # ``tenants`` has a seed row on the target (``t-default``)
            # inserted by the Alembic bootstrap; we don't truncate it
            # in the default path so use ON CONFLICT DO NOTHING to
            # absorb the re-insert cleanly.
            on_conflict = None
            if table == "tenants" and not args.truncate_target:
                on_conflict = "ON CONFLICT (id) DO NOTHING"

            copied = await copy_rows_to_target(
                pg_conn, table, cols, rows,
                batch_size=args.batch_size,
                on_conflict=on_conflict,
            )
            report.tables.append(TableResult(
                table=table, source_rows=len(rows),
                copied_rows=copied, columns=cols,
            ))
            if not args.quiet:
                logger.info(
                    "copied %6d/%-6d rows -> %s",
                    copied, len(rows), table,
                )

        # ── Reset IDENTITY sequences ──
        identity_tables = [t for t in TABLES_WITH_IDENTITY_ID if t in effective]
        report.sequences_reset = await reset_identity_sequences(
            pg_conn, identity_tables,
        )

        # ── Row count parity ──
        mismatch: list[tuple[str, int, int]] = []
        for t in report.tables:
            if t.skipped:
                continue
            n = await count_target_rows(pg_conn, t.table)
            if n != t.source_rows:
                mismatch.append((t.table, t.source_rows, n))
        if mismatch:
            for table, src, tgt in mismatch:
                logger.error(
                    "row count mismatch: %s source=%d target=%d",
                    table, src, tgt,
                )
            report.exit_code = 6
            return report

        # ── Target chain post-flight ──
        if "audit_log" in effective and not args.skip_chain_verify:
            ok, tenant_report = await verify_target_chain(pg_conn)
            report.target_chain_ok = ok
            report.target_chain_tenants = tenant_report
            if not ok:
                report.exit_code = 5
                return report

        report.exit_code = 0
        return report
    finally:
        try:
            await sqlite_conn.close()
        except Exception:
            pass
        if pg_conn is not None:
            try:
                await pg_conn.close()
            except Exception:
                pass
        report.duration_seconds = time.monotonic() - t0


# ─────────────────────────────────────────────────────────────────────────
#  CLI entry points.
# ─────────────────────────────────────────────────────────────────────────


def _emit_report(report: MigrationReport, *, as_json: bool) -> None:
    if as_json:
        print(json.dumps(report.to_dict(), ensure_ascii=False, sort_keys=True))
        return
    print(f"source: {report.source_url}")
    if report.target_url:
        print(f"target: {report.target_url}")
    print(f"dry-run: {report.dry_run}")
    print(f"truncated_target: {report.truncated_target}")
    print(f"source chain ok: {report.source_chain_ok}")
    if report.target_chain_ok is not None:
        print(f"target chain ok: {report.target_chain_ok}")
    for t in report.tables:
        if t.skipped:
            print(f"  [skip] {t.table}: {t.skipped_reason}")
        else:
            print(f"  [ok  ] {t.table}: {t.copied_rows}/{t.source_rows} rows")
    if report.sequences_reset:
        print("sequences reset:")
        for tbl, val in sorted(report.sequences_reset.items()):
            print(f"  {tbl} = {val}")
    print(f"duration: {report.duration_seconds:.3f}s")
    print(f"exit code: {report.exit_code}")


def main(argv: Sequence[str] | None = None) -> int:
    logging.basicConfig(
        level=logging.INFO,
        format="%(asctime)s %(levelname)s %(name)s: %(message)s",
    )
    args = build_argparser().parse_args(argv)
    try:
        # Argument validation that doesn't need a connection.
        parse_source_arg(args.source)
        if not args.dry_run:
            parse_target_arg(args.target)
    except (ValueError, MalformedURLError, UnsupportedURLError) as exc:
        print(f"error: {exc}", file=sys.stderr)
        return 2

    try:
        report = asyncio.run(_run_migration(args))
    except FileNotFoundError as exc:
        print(f"error: {exc}", file=sys.stderr)
        return 1
    except (MalformedURLError, UnsupportedURLError, ValueError) as exc:
        print(f"error: {exc}", file=sys.stderr)
        return 2
    except RuntimeError as exc:
        print(f"error: {exc}", file=sys.stderr)
        return 1
    _emit_report(report, as_json=bool(args.json))
    return report.exit_code


if __name__ == "__main__":
    sys.exit(main(sys.argv[1:]))
